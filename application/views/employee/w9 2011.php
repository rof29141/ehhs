        <div style="float: left; width: 380px;">
            <p style="margin-top: 7px; margin-bottom: 0px;">
                The person who gives Form W-9 to the partnership for purposes of establishing its U.S. status and avoiding withholding on its allocable share of net income from the partnership conducting a trade or business in the United States is in the following cases:
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                • The U.S. owner of a disregarded entity and not the entity,
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                • The U.S. grantor or other owner of a grantor trust and not the trust, and
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                • The U.S. trust (other than a grantor trust) and not the beneficiaries of the trust.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <span style="font-weight: bold;">Foreign person.</span> If you are a foreign person, do not use Form W-9. Instead, use the appropriate Form W-8 (see Publication 515, Withholding of Tax on Nonresident Aliens and Foreign Entities).
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <span style="font-weight: bold;">Nonresident alien who becomes a resident alien.</span> Generally, only a nonresident alien individual may use the terms of a tax treaty to reduce or eliminate U.S. tax on certain types of income. However, most tax treaties contain a provision known as a “saving clause.” Exceptions specified in the saving clause may permit an exemption from tax to continue for certain types of income even after the payee has otherwise become a U.S. resident alien for tax purposes.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                If you are a U.S. resident alien who is relying on an exception contained in the saving clause of a tax treaty to claim an exemption from U.S. tax on certain types of income, you must attach a statement to Form W-9 that specifies the following five items:
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                1. The treaty country. Generally, this must be the same treaty under which you claimed exemption from tax as a nonresident alien.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                2. The treaty article addressing the income.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                3. The article number (or location) in the tax treaty that contains the saving clause and its exceptions.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                4. The type and amount of income that qualifies for the exemption from tax.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                5. Sufficient facts to justify the exemption from tax under the terms of the treaty article.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <span style="font-weight: bold; font-style: italic;">Example.</span> Article 20 of the U.S.-China income tax treaty allows an exemption from tax for scholarship income received by a Chinese student temporarily present in the United States. Under U.S. law, this student will become a resident alien for tax purposes if his or her stay in the United States exceeds 5 calendar years. However, paragraph 2 of the first Protocol to the U.S.-China treaty (dated April 30, 1984) allows the provisions of Article 20 to continue to apply even after the Chinese student becomes a resident alien of the United States. A Chinese student who qualifies for this exception (under paragraph 2 of the first protocol) and is relying on this exception to claim an exemption from tax on his or her scholarship or fellowship income would attach to Form W-9 a statement that includes the information described above to support that exemption.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                If you are a nonresident alien or a foreign entity not subject to backup withholding, give the requester the appropriate completed Form W-8.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <span style="font-weight: bold;">What is backup withholding?</span> Persons making certain payments to you must under certain conditions withhold and pay to the IRS a percentage of such payments. This is called “backup withholding.” Payments that may be subject to backup withholding include interest, tax-exempt interest, dividends, broker and barter exchange transactions, rents, royalties, nonemployee pay, and certain payments from fishing boat operators. Real estate transactions are not subject to backup withholding.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                You will not be subject to backup withholding on payments you receive if you give the requester your correct TIN, make the proper certifications, and report all your taxable interest and dividends on your tax return.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px; font-weight: bold; font-size: 10pt;">
                Payments you receive will be subject to backup withholding if:
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                1. You do not furnish your TIN to the requester,
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                2. You do not certify your TIN when required (see the Part II instructions on page 3 for details),
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                3. The IRS tells the requester that you furnished an incorrect TIN,
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                4. The IRS tells you that you are subject to backup withholding because you did not report all your interest and dividends on your tax return (for reportable interest and dividends only), or
            </p>

            <p style="margin-top: 7px; margin-bottom: 4px;">
                5. You do not certify to the requester that you are not subject to backup withholding under 4 above (for reportable interest and dividend accounts opened after 1983 only).
            </p>
        </div>

        <div style="float: left; width: 380px; margin-left: 20px;">
            <p style="margin-top: 7px; margin-bottom: 0px;">
                Certain payees and payments are exempt from backup withholding. See the instructions below and the separate Instructions for the Requester of Form W-9.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                Also see Special rules for partnerships on page 1.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px; font-weight: bold; font-size: 10pt;">
                Updating Your Information
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                You must provide updated information to any person to whom you claimed to be an exempt payee if you are no longer an exempt payee and anticipate receiving reportable payments in the future from this person. For example, you may need to provide updated information if you are a C corporation that elects to be an S corporation, or if you no longer are tax exempt. In addition, you must furnish a new Form W-9 if the name or TIN changes for the account, for example, if the grantor of a grantor trust dies.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px; font-weight: bold; font-size: 10pt;">
                Penalties
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Failure to furnish TIN.</b> If you fail to furnish your correct TIN to a requester, you are subject to a penalty of $50 for each such failure unless your failure is due to reasonable cause and not to willful neglect.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Civil penalty for false information with respect to withholding.</b> If you make a false statement with no reasonable basis that results in no backup withholding, you are subject to a $500 penalty.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Criminal penalty for falsifying information.</b> Willfully falsifying certifications or affirmations may subject you to criminal penalties including fines and/or imprisonment.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Misuse of TINs.</b> If the requester discloses or uses TINs in violation of federal law, the requester may be subject to civil and criminal penalties.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px; font-weight: bold; font-size: 13pt;">
                Specific Instructions
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px; font-weight: bold; font-size: 10pt;">
                Name
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                If you are an individual, you must generally enter the name shown on your income tax return. However, if you have changed your last name, for instance, due to marriage without informing the Social Security Administration of the name change, enter your first name, the last name shown on your social security card, and your new last name.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                If the account is in joint names, list first, and then circle, the name of the person or entity whose number you entered in Part I of the form.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Sole proprietor.</b> Enter your individual name as shown on your income tax return on the “Name” line. You may enter your business, trade, or “doing business as (DBA)” name on the “Business name/disregarded entity name” line.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Partnership, C Corporation, or S Corporation.</b> Enter the entity's name on the “Name” line and any business, trade, or “doing business as (DBA) name” on the “Business name/disregarded entity name” line.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Disregarded entity.</b> Enter the owner's name on the “Name” line. The name of the entity entered on the “Name” line should never be a disregarded entity. The name on the “Name” line must be the name shown on the income tax return on which the income will be reported. For example, if a foreign LLC that is treated as a disregarded entity for U.S. federal tax purposes has a domestic owner, the domestic owner's name is required to be provided on the “Name” line. If the direct owner of the entity is also a disregarded entity, enter the first owner that is not disregarded for federal tax purposes. Enter the disregarded entity's name on the “Business name/disregarded entity name” line. If the owner of the disregarded entity is a foreign person, you must complete an appropriate Form W-8.
            </p>

            <p style="margin-top: 7px; margin-bottom: 0px;">
                <b>Note.</b> Check the appropriate box for the federal tax classification of the person whose name is entered on the “Name” line (Individual/sole proprietor, Partnership, C Corporation, S Corporation, Trust/estate).
            </p>

            <p style="margin-top: 7px; margin-bottom: 4px;">
                <b>Limited Liability Company (LLC).</b> If the person identified on the “Name” line is an LLC, check the “Limited liability company” box only and enter the appropriate code for the tax classification in the space provided. If you are an LLC that is treated as a partnership for federal tax purposes, enter “P” for partnership. If you are an LLC that has filed a Form 8832 or a Form 2553 to be taxed as a corporation, enter “C” for C corporation or “S” for S corporation. If you are an LLC that is disregarded as an entity separate from its owner under Regulation section 301.7701-3 (except for employment and excise tax), do not check the LLC box unless the owner of the LLC (required to be identified on the “Name” line) is another LLC that is not disregarded for federal tax purposes. If the LLC is disregarded as an entity separate from its owner, enter the appropriate tax classification of the owner identified on the “Name” line.
            </p>
