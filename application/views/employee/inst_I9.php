<h3>Photocopying and Retaining Form I-9</h3>

<p>A blank Form I-9 may be reproduced, provided all sides are copied. The
    instructions and Lists of Acceptable Documents must be available to all
    employees completing this form. Employers must retain each employee's
    completed Form I-9 for as long as the individual works for the employer.
    Employers are required to retain the pages of the form on which the employee
    and employer enter data. If copies of documentation presented by the
    employee are made, those copies must also be kept with the form. Once the
    individual's employment ends, the employer must retain this form for either
    3 years after the date of hire or 1 year after the date employment ended,
    whichever is later.</p>

<p>Form I-9 may be signed and retained electronically, in compliance with
    Department of Homeland Security regulations at
    8 CFR 274a.2.</p>

<h3>USCIS Privacy Act Statement</h3>

<h5>Authorities</h5>
<p>The authority for collecting this information is the
    Immigration Reform and Control Act of 1986,
    Public Law 99-603 (8 USC 1324a).</p>

<h5>Purpose</h5>
<p>This information is collected by employers to comply with the
    requirements of the Immigration Reform and
    Control Act of 1986. This law requires that employers verify the identity
    and employment authorization of individuals
    they hire for employment to preclude the unlawful hiring, or recruiting or
    referring for a fee, of aliens who are not
    authorized to work in the United States.</p>

<h5>Disclosure</h5>
<p>Submission of the information required in this form is
    voluntary. However, failure of the employer to
    ensure proper completion of this form for each employee may result in the
    imposition of civil or criminal penalties. In
    addition, employing individuals knowing that they are unauthorized to work
    in the United States may subject the
    employer to civil and/or criminal penalties.</p>

<h5>Routine Uses</h5>
<p>This information will be used by employers as a record of
    their basis for determining eligibility of an
    employee to work in the United States. The employer will keep this form and
    make it available for inspection by
    authorized officials of the Department of Homeland Security, Department of
    Labor, and Office of Special Counsel for
    Immigration-Related Unfair Employment Practices.</p>

