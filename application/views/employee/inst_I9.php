<p><strong>Anti-Discrimination Notice.</strong> It is illegal to
    discriminate against any work-authorized individual in hiring, discharge,
    recruitment or referral for a fee, or in the employment eligibility
    verification (Form I-9 and E-Verify) process based on that individual's
    citizenship status, immigration status or national origin.  Employers
    <strong>CANNOT</strong> specify which document(s) they will accept from an
    employee. The refusal to hire an individual because the documentation
    presented has a future expiration date may also constitute illegal
    discrimination. For more information, call the Office of Special Counsel
    for Immigration-Related Unfair Employment Practices (OSC) at
    1-800-255-7688 (employees), 1-800-255-8155 (employers), or 1-800-237-2515
    (TDD), or visit <a
        href="http://www.justice.gov/crt/about/osc">www.justice.gov/crt/about/osc</a>.</p>

<h3>What Is the Purpose of This Form?</h3>
<p>Employers must complete Form I-9 to document verification of the identity
    and employment authorization of each new employee (both citizen and
    noncitizen) hired after November 6, 1986, to work in the United States.
    In the Commonwealth of the Northern Mariana Islands (CNMI), employers
    must complete Form I-9 to document verification of the identity and
    employment authorization of each new employee (both citizen and
    noncitizen) hired after November 27, 2011. Employers should have used
    Form I-9 CNMI between November 28, 2009 and November 27, 2011.</p>

<h3>General Instructions</h3>
<p>Employers are responsible for completing and retaining Form I-9. For the
    purpose of completing this form, the term
    "employer" means all employers, including those recruiters and referrers
    for a fee who are agricultural associations,
    agricultural employers, or farm labor contractors.</p>

<p>Form I-9 is made up of three sections. Employers may be fined if the form
    is not complete. Employers are responsible for
    retaining completed forms. Do not mail completed forms to U.S.
    Citizenship and Immigration Services (USCIS) or
    Immigration and Customs Enforcement (ICE).</p>

<h3>Section 1. Employee Information and Attestation</h3>

<p>Newly hired employees must complete and sign Section 1 of Form I-9
    <strong>no later than the first day of employment.</strong>
    Section 1 should never be completed before the employee has accepted a
    job offer.</p>

<p>Provide the following information to complete Section 1:</p>

<dl>
    <dt>Name</dt>
    <dd>Provide your full legal last name, first name, and middle initial.
        Your last name is your family name or
        surname. If you have two last names or a hyphenated last name, include
        both names in the last name field. Your first
        name is your given name. Your middle initial is the first letter of your
        second given name, or the first letter of your
        middle name, if any.</dd>

    <dt>Other names used</dt>
    <dd>Provide all other names used, if any (including maiden name). If you
        have had no other legal
        names, write "N/A."</dd>

    <dt>Address</dt>
    <dd>Provide the address where you currently live, including Street
        Number and Name, Apartment Number (if
        applicable), City, State, and Zip Code. Do not provide a post office box
        address (P.O. Box). Only border commuters
        from Canada or Mexico may use an international address in this
        field.</dd>

    <dt>Date of Birth</dt>
    <dd>Provide your date of birth in the mm/dd/yyyy format. For example,
        January 23, 1950, should be
        written as 01/23/1950.</dd>

    <dt>U.S. Social Security Number</dt>
    <dd>Provide your 9-digit Social Security number. Providing your Social
        Security number
        is voluntary. However, if your employer participates in E-Verify, you
        must provide your Social Security number.</dd>

    <dt>E-mail Address and Telephone Number (Optional)</dt>
    <dd>You may provide your e-mail address and telephone
        number. Department of Homeland Security (DHS) may contact you if DHS
        learns of a potential mismatch between
        the information provided and the information in DHS or Social Security
        Administration (SSA) records. You may write
        "N/A" if you choose not to provide this information.</dd>
</dl>

<p>All employees must attest in Section 1, under penalty of perjury, to
    their citizenship or immigration status by checking
    one of the following four boxes provided on the form:</p>

<ol>
    <li><strong>A citizen of the United States</strong></li>

    <li><strong>A noncitizen national of the United States:</strong>
        Noncitizen nationals of the United States are persons born in
        American Samoa, certain former citizens of the former Trust
        Territory of the Pacific Islands, and certain children of noncitizen
        nationals born abroad.</li>

    <li><strong>A lawful permanent resident:</strong>
        A lawful permanent resident is any person who is not a U.S. citizen
        and who resides in the United States under legally recognized and
        lawfully recorded permanent residence as an immigrant. The term
        "lawful permanent resident" includes conditional residents. If you
        check this box, write either your Alien Registration Number
        (A-Number) or USCIS Number in the field next to your selection. At
        this time, the USCIS Number is the same as the A-Number without the
        "A" prefix.</li>

    <li>
        <p><strong>An alien authorized to work:</strong>
            If you are not a citizen or national of the United States or a
            lawful permanent resident, but are authorized to work in the United
            States, check this box.</p>

        <p>If you check this box:</p>
        <ol>
            <li>Record the date that your employment authorization expires,
                if any. Aliens whose employment authorization does not
                expire, such as refugees, asylees, and certain citizens of
                the Federated States of Micronesia, the Republic of the
                Marshall Islands, or Palau, may write "N/A" on this
                line.</li>

            <li>Next, enter your Alien Registration Number (A-Number)/USCIS
                Number. At this time, the USCIS Number is the same as your
                A-Number without the "A" prefix. If you have not received an
                A-Number/USCIS Number, record your Admission Number. You can
                find your Admission Number on Form I-94, "Arrival-Departure
                Record," or as directed by USCIS or U.S. Customs and Border
                Protection (CBP).

                <ol>
                    <li>If you obtained your admission number from CBP in
                        connection with your arrival in the United States,
                        then also record information about the foreign
                        passport you used to enter the United States (number
                        and country of issuance).</li>

                    <li>If you obtained your admission number from USCIS
                        within the United States, or you entered the United
                        States without a foreign passport, you must write
                        "N/A" in the Foreign Passport Number and Country of
                        Issuance fields.</li>
                </ol>
            </li>
        </ol>
    </li>
</ol>

<p>Sign your name in the "Signature of Employee" block and record the date
    you completed and signed Section 1. By signing and dating this form, you
    attest that the citizenship or immigration status you selected is
    correct and that you are aware that you may be imprisoned and/or fined
    for making false statements or using false documentation when completing
    this form. To fully complete this form, you must present to your
    employer documentation that establishes your identity and employment
    authorization. Choose which documents to present from the Lists of
    Acceptable Documents, found on the last page of this form. You must
    present this documentation no later than the third day after beginning
    employment, although you may present the required documentation before
    this date.</p>

<h4>Preparer and/or Translator Certification</h4>
<p>The Preparer and/or Translator Certification must be completed if the
    employee requires assistance to complete Section 1 (e.g., the employee
    needs the instructions or responses translated, someone other than the
    employee fills out the information blocks, or someone with disabilities
    needs additional assistance). The employee must still sign Section
    1.</p>

<h4>Minors and Certain Employees with Disabilities (Special Placement)</h4>
<p>Parents or legal guardians assisting minors (individuals under 18) and
    certain employees with disabilities should review the guidelines in the
    <em>Handbook for Employers: Instructions for
        Completing Form I-9 (M-274)</em> on
    <a href="http://www.uscis.gov/i-9-central">www.uscis.gov/ I-9Central</a>
    before completing Section 1. These individuals have special procedures
    for establishing identity if they cannot present an identity document
    for Form I-9. The special procedures include <strong>(1)</strong> the
    parent or legal guardian filling out Section 1 and writing "minor under
    age 18" or "special placement," whichever applies, in the employee
    signature block; and<strong>(2)</strong> the employer writing "minor
    under age 18" or "special placement" under List B in Section 2.</p>

<p>Before completing Section 2, employers must ensure that Section 1 is
    completed properly and on time. Employers may not ask an individual to
    complete Section 1 before he or she has accepted a job offer.</p>

<p>Employers or their authorized representative must complete Section 2 by
    examining evidence of identity and employment authorization within 3
    business days of the employee's first day of employment. For example, if
    an employee begins employment on Monday, the employer must complete
    Section 2 by Thursday of that week. However, if an employer hires an
    individual for less than 3 business days, Section 2 must be completed no
    later than the first day of employment. An employer may complete Form
    I-9 before the first day of employment if the employer has offered the
    individual a job and the individual has accepted.</p>

<p>Employers cannot specify which document(s) employees may present from the
    Lists of Acceptable Documents, found on the last page of Form I-9, to
    establish identity and employment authorization. Employees must present
    one selection from List A <strong>OR</strong> a combination of one
    selection from List B and one selection from List C. List A contains
    documents that show both identity and employment authorization. Some
    List A documents are combination documents. The employee must present
    combination documents together to be considered a List A document. For
    example, a foreign passport and a Form I-94 containing an endorsement of
    the alien's nonimmigrant status must be presented together to be
    considered a List A document. List B contains documents that show
    identity only, and List C contains documents that show employment
    authorization only. If an employee presents a List A document, he or she
    should <strong>not</strong> present a List B and List C document, and
    vice versa. If an employer participates in E-Verify, the List B document
    must include a photograph.</p>

<p>In the field below the Section 2 introduction, employers must enter the
    last name, first name and middle initial, if any, that the employee
    entered in Section 1. This will help to identify the pages of the form
    should they get separated.</p>

<p>Employers or their authorized representative must:</p>

<ol>
    <li>Physically examine each original document the employee presents to
        determine if it reasonably appears to be genuine and to relate to
        the person presenting it. The person who examines the documents must
        be the same person who signs Section 2. The examiner of the
        documents and the employee must both be physically present during
        the examination of the employee's documents.</li>

    <li>
        <p>Record the document title shown on the Lists of Acceptable
            Documents, issuing authority, document number and
            expiration date (if any) from the original document(s) the
            employee presents. You may write "N/A" in any unused
            fields.</p>

        <p>If the employee is a student or exchange visitor who presented a
            foreign passport with a Form I-94, the employer should also
            enter in Section 2:</p>

        <ol>
            <li>The student's Form I-20 or DS-2019 number (Student and
                Exchange Visitor Information System-SEVIS Number);
                <strong>and</strong> the program end date from Form I-20 or
                DS-2019.</li>
        </ol>
    </li>

    <li>Under Certification, enter the employee's first day of employment.
        Temporary staffing agencies may enter the first day the employee was
        placed in a job pool. Recruiters and recruiters for a fee do not
        enter the employee's first day of employment.</li>

    <li>Provide the name and title of the person completing Section 2 in the
        Signature of Employer or Authorized Representative field.</li>

    <li>Sign and date the attestation on the date Section 2 is
        completed.</li>

    <li>Record the employer's business name and address.</li>

    <li>Return the employee's documentation.</li>
</ol>

<p>Employers may, but are not required to, photocopy the document(s)
    presented. If photocopies are made, they should be made for
    <strong>ALL</strong> new hires or reverifications. Photocopies must be
    retained and presented with Form I-9 in case of an inspection by DHS or
    other federal government agency. Employers must always complete Section
    2 even if they photocopy an employee's document(s). Making photocopies
    of an employee's document(s) cannot take the place of completing Form
    I-9. Employers are still responsible for completing and retaining Form
    I-9.</p>

<h4>Unexpired Documents</h4>

<p>Generally, only unexpired, original documentation is acceptable. The only
    exception is that an employee may present a certified copy of a birth
    certificate. Additionally, in some instances, a document that appears to
    be expired may be acceptable if the expiration date shown on the face of
    the document has been extended, such as for individuals with temporary
    protected status. Refer to the <em>Handbook for Employers: Instructions
        for Completing Form I-9 (M-274)</em> or I-9 Central (<a
        href="http://www.uscis.gov/i-9-central">www.uscis.gov/I-9Central</a>)
    for examples.</p>

<h4>Receipts</h4>
<p>If an employee is unable to present a required document (or documents),
    the employee can present an acceptable receipt in lieu of a document
    from the Lists of Acceptable Documents on the last page of this form.
    Receipts showing that a person has applied for an initial grant of
    employment authorization, or for renewal of employment authorization,
    are not acceptable. Employers cannot accept receipts if employment will
    last less than 3 days. Receipts are acceptable when completing Form I-9
    for a new hire or when reverification is required.</p>

<p>Employees must present receipts within 3 business days of their first
    day of employment, or in the case of reverification, by the date
    that reverification is required, and must present valid replacement
    documents within the time frames described below.</p>

<p>There are three types of acceptable receipts:</p>
<ol>
    <li>A receipt showing that the employee has applied to replace a
        document that was lost, stolen or damaged. The employee must present
        the actual document within 90 days from the date of hire.</li>

    <li>The arrival portion of Form I-94/I-94A with a temporary I-551 stamp
        and a photograph of the individual. The employee must present the
        actual Permanent Resident Card (Form I-551) by the expiration date
        of the temporary I-551 stamp, or, if there is no expiration date,
        within 1 year from the date of issue.</li>

    <li>The departure portion of Form I-94/I-94A with a refugee admission
        stamp. The employee must present an unexpired Employment
        Authorization Document (Form I-766) or a combination of a List B
        document and an unrestricted Social Security card within 90
        days.</li>

</ol>

<p>When the employee provides an acceptable receipt, the employer
    should:</p>

<ol>
    <li>Record the document title in Section 2 under the sections titled
        List A, List B, or List C, as applicable.</li>

    <li>Write the word "receipt" and its document number in the "Document
        Number" field. Record the last day that the
        receipt is valid in the "Expiration Date" field.</li>
</ol>

<p>By the end of the receipt validity period, the employer should:</p>
<ol>
    <li>Cross out the word "receipt" and any accompanying document
        number and expiration date.</li>

    <li>Record the number and other required document information
        from the actual document presented.</li>

    <li>Initial and date the change.</li>
</ol>

<p>See the <em>Handbook for Employers: Instructions for Completing Form I-9
        (M-274)</em> at <a
        href="http://www.uscis.gov/i-9-central">www.uscis.gov/I-9Central</a>
    for more information on receipts.</p>

<h3>Section 3. Reverification and Rehires</h3>

<p>Employers or their authorized representatives should complete Section 3
    when reverifying that an employee is authorized to work. When rehiring
    an employee within 3 years of the date Form I-9 was originally
    completed, employers have the option to complete a new Form I-9 or
    complete Section 3. When completing Section 3 in either a reverification
    or rehire situation, if the employee's name has changed, record the name
    change in Block A.</p>

<p>For employees who provide an employment authorization expiration date in
    Section 1, employers must reverify employment authorization on or before
    the date provided.</p>

<p>For employees who provide an employment authorization expiration date in
    Section 1, employers must reverify employment authorization on or before
    the date provided.</p>

<p>Some employees may write "N/A" in the space provided for the expiration
    date in Section 1 if they are aliens whose employment authorization does
    not expire (e.g., asylees, refugees, certain citizens of the Federated
    States of Micronesia, the Republic of the Marshall Islands, or Palau).
    Reverification does not apply for such employees unless they chose to
    present evidence of employment authorization in Section 2 that contains
    an expiration date and requires reverification, such as Form I-766,
    Employment Authorization Document.</p>

<p>Reverification applies if evidence of employment authorization (List A or
    List C document) presented in Section 2 expires. However, employers
    should not reverify:</p>

<ol>
    <li>U.S. citizens and noncitizen nationals; or</li>
    <li>Lawful permanent residents who presented a Permanent Resident Card
        (Form I-551) for Section 2.</li>
</ol>

<p>Reverification does not apply to List B documents.</p>

<p>If both Section 1 and Section 2 indicate expiration dates triggering the
    reverification requirement, the employer should
    reverify by the earlier date.</p>

<p>For reverification, an employee must present unexpired documentation
    from either List A or List C showing he or she is
    still authorized to work. Employers CANNOT require the employee to
    present a particular document from List A or List
    C. The employee may choose which document to present.</p>

<p>To complete Section 3, employers should follow these instructions:</p>

<ol>
    <li>Complete Block A if an employee's name has changed at the time you complete Section 3.</li>

    <li>Complete Block B with the date of rehire if you rehire an employee
        within 3 years of the date this form was originally completed, and
        the employee is still authorized to be employed on the same basis as
        previously indicated on this form.  Also complete the "Signature of
        Employer or Authorized Representative" block.</li>

    <li>Complete Block C if:
        <ol>
            <li>The employment authorization or employment authorization
                document of a current employee is about to expire and
                requires reverification; or</li>

            <li>You rehire an employee within 3 years of the date this form
                was originally completed and his or her employment
                authorization or employment authorization document has
                expired. (Complete Block B for this employee as well.)</li>
        </ol>

        <p>To complete Block C:</p>
        <ol>
            <li>Examine either a List A or List C document the employee
                presents that shows that the employee is currently
                authorized to work in the United States; and</li>

            <li>Record the document title, document number, and expiration
                date (if any).</li>
        </ol>
    </li>

    <li>
        <p>After completing block A, B or C, complete the "Signature of
            Employer or Authorized Representative" block,
            including the date.</p>

        <p>For reverification purposes, employers may either complete
            Section 3 of a new Form I-9 or Section 3 of the previously
            completed Form I-9. Any new pages of Form I-9 completed during
            reverification must be attached to the employee's original Form
            I-9. If you choose to complete Section 3 of a new Form I-9, you
            may attach just the page containing Section 3, with the
            employee's name entered at the top of the page, to the
            employee's original Form I-9. If there is a more current version
            of Form I-9 at the time of reverification, you must complete
            Section 3 of that version of the form.</p>
    </li>
</ol>

<h3>What Is the Filing Fee?</h3>
